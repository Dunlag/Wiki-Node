// Importa el módulo '@colors/colors' que permite añadir colores a las cadenas de texto en la consola.
const colors = require('@colors/colors');
// Importa el módulo 'axios', una librería para realizar peticiones HTTP desde Node.js.
const axios = require('axios');

// Imprime en la consola el texto "hello" en color verde.  El método .green es proporcionado por el módulo colors.
console.log('hello'.green); // outputs green text

// Realiza una petición GET a la API de Rick and Morty para obtener información de personajes.
axios.get('https://rickandmortyapi.com/api/character')
  .then(response => { // Si la petición es exitosa, la función dentro de .then se ejecuta.
    // 'response' contiene la respuesta del servidor. response.data contiene los datos de la respuesta (generalmente en formato JSON).
    console.log(response.data); // Imprime en la consola los datos recibidos de la API.
  })
  .catch(error => { // Si la petición falla (por ejemplo, si el servidor no responde o la URL es incorrecta), la función dentro de .catch se ejecuta.
    console.log(error); // Imprime en la consola el error que ocurrió durante la petición.
  });